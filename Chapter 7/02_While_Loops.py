# While Loop
# Syntax:
# while (condition): # The block keeps executing until the condition is true
#  #Body of the loop
 
# In while loops, the condition is checked first. If it evaluates to true, the body of the loop is executed
# otherwise not! If the loop is entered, the process of [condition check & execution] is continued until the condition
# becomes False.

i = 1

while(i<6):
    print(i)  
    i += 1 # You can also write i = i+!



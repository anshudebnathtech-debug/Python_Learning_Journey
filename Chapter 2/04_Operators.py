# Operators in Python
# Following are some common operators in python:
# 1. Arithmetic operators: +, -, *, / etc.
# 2. Assignment operators: =, +=, -= etc.
# 3. Comparison operators: ==, >, >=, <, != etc.
# 4. Logical operators: and, or, not.

# Arithmatic Operators

a = 34
b = 23
c = a + b  # Here a and b are operands, c is result and = is a operator 
print(c)

# Assignment Operators

a = 4-2 # Assign value in a
print(a)
b = 6
c = 5
b += 3 # Increment the value in b by 3and then assign it to b
c -= 3 # Decrement the value in b by 3and then assign it to b
print(b)
print(c)

# # You also can:
# b *= 2
# c /= 4

# Comparison Operator

d = 5<4
e = 6!=6 # not equal to
f = 5==5 # double equal to
print(d)
print(e)
print(f)

# Logical Operator

g = True or False
print(g)

# Truth table of 'or'
print("True or False is", True or False)
print("True or True is", True or True)
print("False or True is", False or True)
print("False or False is", False or False)

# Truth table of 'and'
print("True and False is", True and False)
print("True and True is", True and True)
print("False and True is", False and True)
print("False and False is", False and False)

print(not(False))
print(not(True))
